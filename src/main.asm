; Binary / media downloads.
; ------------------------------------------------------
; A = gopher item type. Out: ZF=1 if it is a downloadable binary/media type.
; (9 binary, 5 DOS archive, g GIF, I image, s sound, ; video, d document, p PNG.)
IS_BIN_TYPE
	PUSH	BC
	PUSH	HL
	LD		C, A
	LD		HL, BIN_TYPES
.l
	LD		A, (HL)
	OR		A
	JR		Z, .no
	CP		C
	JR		Z, .yes
	INC		HL
	JR		.l
.yes
	XOR		A						; ZF=1
	JR		.done
.no
	OR		1						; ZF=0
.done
	POP		HL
	POP		BC
	RET
BIN_TYPES		DB "95gIs;dp", 0

; Download DL_SEL to DL_PATH through normal ESP-AT +IPD frames. Each frame
; supplies an exact payload length; EOF is accepted only when TCP.RECEIVE
; reports the peer's CLOSED notification.
;
; The WHOLE body is first received into a fresh GetMem page chain (the visible
; page is detached DOC.SAVE_STATE-style for the duration) and written to FAT
; only after CLOSED. Rationale (proven by byte-comparing broken downloads
; against their originals - every one was an exact prefix cut at a 1460-byte
; frame boundary): jesperl's ESP-AT discards its queued +IPD frames when the
; peer's FIN is processed while RTS blocks the UART relay. Any FAT write pauses
; RTS for tens of ms, so a mid-transfer write is a roulette window for the tail
; of the file. With no disk I/O during the network phase the ESP drains at full
; line rate and FIN almost always finds an empty queue - the same reason the
; kit's wget holds its final 8 KB in RAM (we just can't know where "final"
; starts without Content-Length, so we hold everything; cap 256 KB).
DOWNLOAD
	XOR		A
	LD		(WCOMMON.CANCELLED), A	; clear any stale cancel flag
	LD		(dl_disk_err), A
	LD		(dl_partial), A
	LD		HL, 0
	LD		(recv_lo), HL
	LD		(recv_hi), HL			; zero the 32-bit byte counter
	LD		(dl_last_kb), HL		; "Receiving 0 KB" is drawn below via DSS
	CALL	SHOW_FETCHING
	LD		A, (net_inited)
	OR		A
	JR		NZ, .haveinit
	CALL	NET.INIT
	JP		C, .e_init
	LD		A, 1
	LD		(net_inited), A
.haveinit
	LD		HL, DL_HOST
	LD		DE, DL_PORT
	CALL	NET.CONNECT				; active +IPD (CIPMODE=0): the kit's RES_NOT_CONN
	JP		C, .e_conn				; is a reliable EOF (transparent's token was flaky)
	LD		HL, DL_DIRMK			; create the destination before asking for data
	CALL	FILE.ENSURE_DIR
	LD		HL, DL_PATH
	CALL	FILE.CREATE
	JP		C, .e_file
	; Detach the visible page; the download body gets its own page chain.
	LD		HL, LINE_BUF			; free during downloads (no rendering runs)
	CALL	DOC.SAVE_STATE
	CALL	DOC.NEW
	CALL	DOC.RESERVE				; first page now - no GetMem once data streams
	JP		C, .e_mem
	CALL	NET.ACTIVE_PREP			; clear LSR/parser state; RTS up for the '>' prompt
	LD		HL, MSG_RECEIVING_ZERO	; safe DSS draw: selector has not been sent yet
	CALL	SET_STATUS
	LD		HL, DL_SEL
	CALL	BUILD_REQ_HL			; HL=REQ_BUF, BC=len
	CALL	NET.SEND
	JP		C, .e_send
	CALL	DL_RECV_LOOP			; whole body -> page chain via active +IPD
	PUSH	AF						; close the socket on success, timeout, or cancel
	CALL	NET.CLOSE
	POP		AF
	JR		C, .e_stream
	; Network phase over: now persist the body. The connection is closed; FAT
	; writes are safe.
	CALL	SHOW_WRITING
	LD		HL, (DOC.doc_woff)
	LD		(dw_last), HL			; EOF: the last page is written in full
	CALL	DL_WRITE_DOC			; page chain -> file, 4 KB staged chunks
	JR		C, .e_wr
	CALL	DL_RESTORE_DOC			; free the body chain, re-attach the page
	CALL	FILE.CLOSE
	JP		C, .e_close
	OR		A
	RET

.e_wr
	LD		A, 1
	LD		(dl_disk_err), A
.e_stream
	LD		A, (WCOMMON.CANCELLED)
	OR		A
	JR		NZ, .discard			; user aborted: the partial is unwanted
	LD		A, (dl_disk_err)
	OR		A
	JR		NZ, .discard			; UART/disk/memory fault: data not trustworthy
	; A clean prefix was received but the server never closed. Let the user
	; choose: retry from scratch, or keep the partial file.
	LD		A, (recv_lo)
	LD		D, A
	LD		A, (recv_lo + 1)
	OR		D
	LD		D, A
	LD		A, (recv_hi)
	OR		D
	LD		D, A
	LD		A, (recv_hi + 1)
	OR		D
	JR		Z, .discard				; nothing received: nothing worth keeping
	CALL	CONFIRM_KEEP			; CF=0 -> retry, CF=1 -> keep the prefix
	JR		C, .keep
	CALL	DL_RESTORE_DOC
	CALL	FILE.CLOSE
	LD		HL, DL_PATH
	CALL	FILE.DELETE
	JP		DOWNLOAD				; fresh attempt: reconnect, recreate the file
.keep
	LD		A, 1
	LD		(dl_partial), A			; "(partial)" status; no viewer offer
	LD		HL, (DOC.doc_woff)
	LD		(dw_last), HL
	CALL	SHOW_WRITING
	CALL	DL_WRITE_DOC			; persist the received prefix
	JP		C, .e_wr				; -> dl_disk_err=1 -> .discard (no loop)
	CALL	DL_RESTORE_DOC
	CALL	FILE.CLOSE
	JP		C, .e_close
	OR		A						; CF=0: the caller shows "Saved ... (partial)"
	RET
.discard
	CALL	DL_RESTORE_DOC
	CALL	FILE.CLOSE
	LD		HL, DL_PATH
	CALL	FILE.DELETE				; never leave an untrusted partial file behind
	LD		A, (WCOMMON.CANCELLED)
	OR		A
	JR		NZ, .e_cancel
	LD		A, (dl_disk_err)
	CP		2
	JR		Z, .e_uart
	CP		4
	JR		Z, .e_nomem
	OR		A
	JR		NZ, .e_disk
	LD		HL, ERR_INCOMPLETE
	JR		.fail
.e_cancel
	LD		HL, ERR_CANCEL
	JR		.fail
.e_uart
	LD		HL, ERR_UART
	JR		.fail
.e_nomem
	LD		HL, MSG_MEM_ERR
	JR		.fail
.e_init
	LD		HL, ERR_INIT
	JP		FETCH_ERR
.e_conn
	LD		HL, ERR_CONN			; RAW_CONNECT already restored command mode on fail
	JR		.fail
.e_mem
	CALL	NET.CLOSE
	CALL	DL_RESTORE_DOC			; NEW was done; nothing allocated to free
	CALL	FILE.CLOSE
	LD		HL, DL_PATH
	CALL	FILE.DELETE
	LD		HL, MSG_MEM_ERR
	JR		.fail
.e_send
	CALL	NET.CLOSE
	CALL	DL_RESTORE_DOC
	CALL	FILE.CLOSE
	LD		HL, DL_PATH
	CALL	FILE.DELETE
	LD		HL, ERR_SEND
	JR		.fail
.e_file
	CALL	NET.CLOSE				; connected but no file yet -> drop the socket
	LD		HL, ERR_DISK
	JR		.fail
.e_close
	LD		HL, DL_PATH
	CALL	FILE.DELETE
.e_disk
	LD		HL, ERR_DISK
.fail
	JP		FETCH_ERR

; Free the download's page chain and re-attach the page the user was viewing.
; Preserves CF/A (callers are mid error-classification).
DL_RESTORE_DOC
	PUSH	AF
	CALL	DOC.RESET				; free the body chain (or the empty NEW doc)
	LD		HL, LINE_BUF
	CALL	DOC.LOAD_STATE
	POP		AF
	RET

; Receive the whole body into the download page chain over the ACTIVE +IPD pipe
; (CIPMODE=0). EOF is the kit's RES_NOT_CONN - a genuine peer CLOSED and the same
; close signal wget relies on. (Transparent CIPMODE=1 was a regression: its
; byte-scanned "\r\nCLOSED" token often never arrived on this firmware, so the
; download hung then failed as "incomplete"; active +IPD framing restores a
; dependable close - the user confirmed downloads were more stable without it.)
;
; TAIL PROTECTION (the ESP-AT V2.2.1 FIN-drop). This firmware discards up to
; ~1-2 KB of un-relayed +IPD data when the peer's FIN is processed WHILE RTS is
; deasserted - i.e. during a slow append. The kit's own wget documents the same
; drop and beats it by holding the last 8 KB in RAM with RTS asserted across the
; close (it knows the end from Content-Length; gopher has none). We get the same
; "RTS asserted across the close" without a length: bursts ACCUMULATE in DL_BUF
; with RTS held, and DOC.APPEND (the only RTS-deasserting step) runs ONLY when a
; short DL_CONT_TIMEOUT read finds no next frame - the ESP is momentarily DRAINED,
; so that gap has nothing to drop - or mid-stream when the buffer fills (safe:
; data still flowing, FIN not yet pending). A standard gopher server sends the
; whole body then closes, so the last bytes always drain before the FIN and the
; RES_NOT_CONN is then read gap-free. A confirmed close is thus always byte-exact;
; a stall fails visibly (incomplete -> retry/keep), never silent corruption.
; Each read is capped below TCP_ACTIVE_IPD_MAX so the kit returns after one frame
; without its multi-frame peek - the following read then observes CLOSED cleanly.
; Progress is drawn per burst (VRAM, no gap) so it advances < every KB.
; Out: CF=0 + raw_closed=1 on a clean close; CF=1 on timeout/cancel/UART/mem error.
DL_RECV_CAP		EQU 1499				; < TCP_ACTIVE_IPD_MAX: one +IPD frame per read
DL_CONT_TIMEOUT	EQU 250					; ms silence that proves the ESP is drained
DL_ACC_HIGH		EQU DL_BUF_SIZE - DL_RECV_CAP	; flush before the next frame can't fit
DL_RECV_LOOP
	XOR		A
	LD		(raw_closed), A
	LD		(TCP.LSR_ACCUM), A		; own the receive's UART-error accounting
	LD		HL, (WCOMMON.IDLE_CB)
	LD		(recv_idle_cb), HL
	LD		HL, 0
	LD		(WCOMMON.IDLE_CB), HL	; no DSS/BIOS calls inside the kit's byte waits
.l
	; Over the 256 KB page-chain cap? Flush whole sectors to disk and continue
	; (unbounded file size). RX is paused for the write; the ESP backpressures the
	; server via the TCP window - mid-stream, so no FIN is pending.
	LD		A, (DOC.doc_npages)
	CP		DOC_MAX_PAGES
	JR		C, .roomok
	LD		HL, DOC_PAGE_SIZE - DL_BUF_SIZE
	LD		DE, (DOC.doc_woff)
	OR		A
	SBC		HL, DE
	JR		NC, .roomok				; a full DL_BUF still fits the current page
	CALL	DL_MIDFLUSH				; sets dl_disk_err itself on failure
	JP		C, .fail
.roomok
	LD		HL, 0
	LD		(dl_comb), HL			; fill = 0
	LD		A, 1
	LD		(dl_first), A			; first read of the window waits the full timeout
.acc
	CALL	NET.RX_RESUME			; RTS asserted throughout the accumulation
	LD		DE, (dl_comb)			; fill
	LD		HL, DL_BUF
	ADD		HL, DE					; HL = DL_BUF + fill = receive dest
	PUSH	HL
	LD		HL, DL_BUF_SIZE
	OR		A
	SBC		HL, DE					; HL = free = SIZE - fill
	LD		DE, DL_RECV_CAP
	OR		A
	SBC		HL, DE					; free - CAP
	JR		NC, .usecap				; free >= CAP -> read a whole CAP
	ADD		HL, DE					; free < CAP (defensive) -> take the remaining free
	JR		.haveroom
.usecap
	LD		HL, DL_RECV_CAP
.haveroom
	LD		B, H
	LD		C, L					; BC = room (<= CAP, never overruns DL_BUF)
	POP		HL						; HL = dest
	LD		A, (dl_first)
	OR		A
	LD		DE, RECV_TIMEOUT		; first: wait the whole timeout for the response
	JR		NZ, .have_to
	LD		DE, DL_CONT_TIMEOUT		; continuation: short - a miss proves drained
.have_to
	CALL	NET.RECV				; active TCP.RECEIVE; polls Esc/Ctrl+Z internally
	PUSH	AF
	CALL	NET.RX_PAUSE			; drop RTS immediately after the read (wget order)
	POP		AF
	JR		C, .rx_end				; CF=1: CLOSED / timeout / cancel
	LD		HL, (dl_comb)
	ADD		HL, BC
	LD		(dl_comb), HL			; fill += received
	XOR		A
	LD		(dl_first), A			; subsequent reads are continuations
	CALL	DL_PROGRESS_ACC			; per-burst VRAM progress (no append, no gap)
	LD		HL, DL_ACC_HIGH
	LD		DE, (dl_comb)
	OR		A
	SBC		HL, DE
	JR		C, .flush				; accumulator near full -> flush now
	JR		.acc					; room remains -> keep the pipe draining
.rx_end
	; NET.RECV returned CF=1: A = result code.
	CP		RES_NOT_CONN
	JR		Z, .closed				; genuine peer CLOSED = reliable EOF
	LD		A, (WCOMMON.CANCELLED)
	OR		A
	JR		NZ, .silent				; Esc/Ctrl+Z
	; Genuine silence (RES_RS_TIMEOUT). New data this window means the ESP just
	; drained: flush it (safe - nothing queued to drop) and wait for more or the
	; close. Nothing new means the stream stalled with no CLOSED -> incomplete.
	LD		HL, (dl_comb)
	LD		A, H
	OR		L
	JR		Z, .silent
	LD		BC, (dl_comb)
	CALL	DL_APPEND_RUN
	JR		C, .nomem
	JP		.l						; new window; its first read waits the full timeout
.closed
	; A latched UART RX error taints the whole stream (LSR accumulates) -> reject.
	LD		A, (TCP.LSR_ACCUM)
	AND		LSR_OE | LSR_PE | LSR_FE | LSR_BI | LSR_RCVE
	JR		NZ, .uart_fail
	LD		BC, (dl_comb)
	CALL	DL_APPEND_RUN			; append the final accumulated bytes
	JR		C, .nomem
	LD		A, 1
	LD		(raw_closed), A			; clean EOF -> byte-exact
	JR		.done
.flush
	LD		BC, (dl_comb)
	CALL	DL_APPEND_RUN
	JR		C, .nomem
	JP		.l
.nomem
	LD		A, 4
	LD		(dl_disk_err), A
	JR		.fail
.uart_fail
	LD		A, 2
	LD		(dl_disk_err), A
	JR		.fail
.silent
	LD		A, RES_RS_TIMEOUT
	LD		(dl_net_err), A
.fail
	CALL	NET.RX_RESUME			; leave RTS asserted: NET.CLOSE's CIPCLOSE needs a
	LD		HL, (recv_idle_cb)		; reply, and the next connect (RAW or active) too
	LD		(WCOMMON.IDLE_CB), HL
	SCF
	RET
.done
	CALL	NET.RX_RESUME			; the loop's last act was RX_PAUSE; restore RTS so
	LD		HL, (recv_idle_cb)		; the ESP can answer NET.CLOSE and the next fetch
	LD		(WCOMMON.IDLE_CB), HL
	OR		A						; CF=0
	RET

; Append BC bytes from DL_BUF to the document (map WIN3 + LDIR), advance the
; received-byte counter and redraw progress. BC=0 is a no-op. Out: CF=1 if the
; append hit the page cap or a GetMem failure (doc_trunc). Preserves nothing.
DL_APPEND_RUN
	LD		A, B
	OR		C
	RET		Z						; nothing to append -> CF=0
	PUSH	BC
	LD		HL, DL_BUF
	CALL	DOC.APPEND				; map WIN3 + LDIR; GetMem only on a page grow
	POP		BC
	LD		A, (DOC.doc_trunc)
	OR		A
	SCF
	RET		NZ						; page cap / GetMem failure
	CALL	DL_ADD					; received total += BC
	; Progress DURING receive is VRAM-only: a DSS draw here runs with RTS
	; asserted and, like the page loop's deferred drawing, could expose the
	; stream to a mid-draw close. The VRAM paint is DI-guarded and sub-ms.
	LD		HL, 0
	LD		(dl_pending), HL		; appended now -> counted in recv_lo, no pending
	CALL	DL_PROGRESS_RX
	OR		A						; CF=0
	RET

; Mid-transfer chain flush for files larger than the 256 KB page chain: write
; the chain's WHOLE SECTORS out, carry the sub-sector tail (0..511 bytes) into
; a fresh chain, and keep receiving - so every mid-file FAT write stays
; 512-aligned. RX is paused for the whole write - the ESP's TCP window closes
; and the server simply waits.
; Out: CF=1 on failure (dl_disk_err: 1 = write error, 4 = no memory).
DL_MIDFLUSH
	CALL	NET.RX_PAUSE
	CALL	SHOW_WRITING			; DSS draw is safe: RX explicitly paused
	LD		HL, (DOC.doc_woff)
	LD		A, H
	AND		0xFE
	LD		H, A
	LD		L, 0
	LD		(dw_last), HL			; last page: whole sectors go to disk...
	EX		DE, HL
	LD		HL, (DOC.doc_woff)
	OR		A
	SBC		HL, DE
	LD		(dw_rem), HL			; ...the tail carries over (0..511)
	CALL	DL_WRITE_DOC
	JR		C, .werr
	LD		BC, (dw_rem)
	LD		A, B
	OR		C
	JR		Z, .carrydone
	LD		A, (DOC.doc_wpage)
	CALL	DOC.MAP_PAGE			; stage the tail before the chain is freed
	LD		HL, (dw_last)
	LD		DE, DOC_W3
	ADD		HL, DE					; src = last page + aligned prefix
	LD		DE, DL_BUF				; DL_WRITE_DOC is done with DL_BUF by now
	LDIR
.carrydone
	CALL	DOC.RESET				; free the written-out chain
	CALL	DOC.RESERVE				; first page of the next chain
	JR		C, .merr
	LD		BC, (dw_rem)
	LD		A, B
	OR		C
	JR		Z, .ok
	LD		HL, DL_BUF
	CALL	DOC.APPEND				; the tail opens the next chain
.ok
	OR		A
	RET
.werr
	LD		A, 1
	LD		(dl_disk_err), A
	SCF
	RET
.merr
	LD		A, 4
	LD		(dl_disk_err), A
	SCF
	RET

; Write the received page chain to the open file, staged through DL_BUF in 4 KB
; chunks (a DSS call must never read straight from a WIN3-mapped doc page).
; Pages are 16 KB = 4 chunks, so every FAT write is 8 whole sectors except the
; file's final chunk. Out: CF=1 on a DSS write error (A = DSS code).
DL_WRITE_DOC
	XOR		A
	LD		(dw_page), A
.page
	LD		A, (DOC.doc_npages)
	LD		B, A
	LD		A, (dw_page)
	CP		B
	JR		NC, .done				; past the last allocated page
	INC		A
	CP		B						; is dw_page the last page?
	LD		HL, DOC_PAGE_SIZE
	JR		C, .lenok				; not last -> full 16 KB of data
	LD		HL, (dw_last)			; last -> caller-set length (whole page at
.lenok								; EOF; whole sectors only for a mid-flush)
	LD		(dw_len), HL
	LD		HL, 0
	LD		(dw_off), HL
.chunk
	LD		HL, (dw_len)
	LD		DE, (dw_off)
	OR		A
	SBC		HL, DE					; HL = bytes left in this page
	JR		Z, .next
	LD		DE, DL_BUF_SIZE
	PUSH	HL
	OR		A
	SBC		HL, DE
	POP		HL
	JR		C, .n_ok				; remainder < 4 KB -> take it all
	LD		HL, DL_BUF_SIZE
.n_ok
	LD		(dw_n), HL
	LD		A, (dw_page)
	CALL	DOC.MAP_PAGE			; doc page into WIN3
	LD		HL, (dw_off)
	LD		DE, DOC_W3
	ADD		HL, DE					; src inside the mapped page
	LD		DE, DL_BUF
	LD		BC, (dw_n)
	LDIR							; stage to WIN2 (dw_n >= 1 here)
	LD		HL, DL_BUF
	LD		BC, (dw_n)
	CALL	FILE.WRITE				; WIN3 is free again for DSS
	RET		C
	LD		HL, (dw_off)
	LD		BC, (dw_n)
	ADD		HL, BC
	LD		(dw_off), HL
	JR		.chunk
.next
	LD		A, (dw_page)
	INC		A
	LD		(dw_page), A
	JR		.page
.done
	OR		A
	RET

; Add BC to the 32-bit count of received bytes.
DL_ADD
	LD		HL, (recv_lo)
	ADD		HL, BC
	LD		(recv_lo), HL
	RET		NC
	LD		HL, (recv_hi)
	INC		HL
	LD		(recv_hi), HL
	RET

; Per-burst progress during accumulation: show recv_lo (appended) plus the
; bytes accumulated this window but not yet appended (the whole fill). VRAM only
; (no gap). Clobbers A/BC/DE/HL.
DL_PROGRESS_ACC
	LD		HL, (dl_comb)			; fill = un-appended bytes this window
	LD		(dl_pending), HL
	CALL	DL_PROGRESS_RX
	LD		HL, 0
	LD		(dl_pending), HL		; leave it zeroed for the flush/final draw
	RET

; In-stream progress. Paints "Receiving <n> KB" straight into text VRAM: no
; DSS/BIOS call and no RTS pause. The displayed byte total is recv_lo/recv_hi
; (appended) + dl_pending (accumulated-but-not-appended). The ~0.2 ms VRAM
; burst is covered by the TL16C550 auto-RTS (AFE). Clobbers A/BC/DE/HL.
DL_PROGRESS_RX
	LD		HL, (recv_lo)			; total = recv_lo + dl_pending ...
	LD		DE, (dl_pending)
	ADD		HL, DE
	LD		A, H					; A = total_lo >> 8
	LD		HL, (recv_hi)
	LD		DE, 0
	ADC		HL, DE					; HL = total_hi
	; KB = (hi16 << 6) | (lo16 >> 10), good to 64 MB
	SRL		A
	SRL		A						; A = lo16 >> 10 (0..63)
	ADD		HL, HL
	ADD		HL, HL
	ADD		HL, HL
	ADD		HL, HL
	ADD		HL, HL
	ADD		HL, HL					; HL = hi16 * 64
	LD		E, A
	LD		D, 0
	ADD		HL, DE
	EX		DE, HL					; DE = KB received
	LD		HL, (dl_last_kb)
	PUSH	DE
	EX		DE, HL					; HL = KB, DE = last drawn
	OR		A
	SBC		HL, DE					; HL = delta (KB only grows)
	POP		DE						; DE = KB
	RET		Z						; same KB -> nothing to redraw
	LD		(dl_last_kb), DE
	PUSH	DE
	LD		HL, DLP_NUM				; blank the variable part (erases a longer
	LD		B, DLP_NUM_LEN			; previous number and the version tail of
.blank								; the initial DSS-drawn status)
	LD		(HL), ' '
	INC		HL
	DJNZ	.blank
	POP		HL						; KB value
	LD		DE, DLP_NUM
	CALL	UTIL.UTOA				; decimal digits + NUL; DE -> past the NUL
	EX		DE, HL
	DEC		HL
	LD		(HL), ' '				; overwrite the NUL with " KB"
	INC		HL
	LD		(HL), 'K'
	INC		HL
	LD		(HL), 'B'
	LD		A, (dlp_base)
	; fall through to the VRAM paint, A = PORT_Y of the first cell

; Paint DLP_TXT onto the status row via direct text VRAM access (see
; console.inc): WIN3 -> page #50, PORT_Y selects the column (base derived from
; DSS GetVMod by INIT_PROGRESS_VRAM), symbol at #C301+row*4, attribute right
; after it. DI around the burst - an interrupt handler could remap WIN3 or
; PORT_Y between select and write.
DLP_ROW_SYM		EQU 0xC301 + STATUS_ROW * 4
DLP_DRAW
	LD		HL, DLP_TXT
	LD		B, DLP_TXT_LEN			; cell count
	LD		C, A					; PORT_Y of column 1
	DI
	IN		A, (PAGE3)
	PUSH	AF						; save the current WIN3 page
	LD		A, VRAM_TXT_PAGE
	OUT		(PAGE3), A
.cell
	LD		A, C
	OUT		(PORT_Y), A				; select the column
	LD		A, (HL)
	LD		(DLP_ROW_SYM), A
	LD		A, ATTR_STATUS
	LD		(DLP_ROW_SYM + 1), A
	INC		HL
	INC		C
	DJNZ	.cell
	LD		A, VRAM_PORTY_PARK
	OUT		(PORT_Y), A				; park PORT_Y outside the visible area
	POP		AF
	OUT		(PAGE3), A				; restore WIN3
	EI
	RET

; WCOMMON.INIT_VMODE has already selected 80x32. DSS GetVMod returns the active
; screen in B (0/1). In this mode BIOS H_BEG is #01 for screen 0 and #81 for
; screen 1; our status text starts at logical column 1, hence PORT_Y #02/#82.
; Store both values once at startup. No probing or DSS call is needed while a
; binary transfer is active.
INIT_PROGRESS_VRAM
	LD		C, DSS_GETVMOD
	RST		DSS						; A=mode (#03), B=active screen (0/1)
	LD		A, B
	AND		1
	LD		(video_page), A
	RRCA							; screen 1 -> bit 7
	OR		0x02					; logical status column 1
	LD		(dlp_base), A
	RET

; Status for the post-receive disk phase: "Writing <n> KB". Network is idle and
; RX is paused here, so an ordinary DSS draw is safe.
SHOW_WRITING
	CALL	STATUS_HOME
	LD		HL, MSG_WRITING
	CALL	TERM.PUTS
	JP		PUT_SIZE_RV

; Final status after a successful download: "Saved <n> KB to <path>", with a
; " (partial)" suffix when the user chose to keep an incomplete transfer.
SHOW_SAVED
	CALL	STATUS_HOME
	LD		HL, MSG_SAVED
	CALL	TERM.PUTS
	CALL	PUT_SIZE_RV
	LD		HL, MSG_TO
	CALL	TERM.PUTS
	LD		HL, DL_PATH
	CALL	TERM.PUTS
	LD		A, (dl_partial)
	OR		A
	RET		Z
	LD		HL, MSG_PARTIAL_SFX
	JP		TERM.PUTS

; After a successful download: if GOPHER.CFG maps the file's extension to a
; program, optionally ask the user (unless the "skip_ask_for_exec" setting is on)
; and launch it via Dss.Exec with %file% = the saved file's path.
MAYBE_OPEN_DOWNLOAD
	LD		A, (dl_partial)
	OR		A
	RET		NZ						; never offer a viewer for a kept partial file
	CALL	DL_EXT_PTR				; HL = extension of DL_NAME (empty if none)
	CALL	CFG.VIEWER_FOR_EXT		; HL=ext -> DE=command template, CF=1 if none
	RET		C						; no association -> leave the "Saved ..." status
	LD		(open_tpl), DE
	CALL	SKIP_ASK				; CF=1 -> launch without asking
	JR		C, .launch
	CALL	CONFIRM_OPEN			; CF=1 -> user declined
	JR		NC, .launch
	CALL	SHOW_DOC_STATUS			; restore the status line after the prompt
	RET
.launch
	CALL	BUILD_FILE_ABS			; FILE_ABS = absolute path of the saved file
	LD		HL, (open_tpl)
	LD		DE, FILE_ABS
	CALL	CFG.BUILD_CMD			; HL = CMD_BUF ("%file%" expanded): "prog arg"
	CALL	EXEC_PROGRAM			; run the viewer directly (full EXE path), like FN
	PUSH	AF						; CF = launch error
	CALL	REDRAW_FULL				; the viewer took over the screen - repaint
	POP		AF
	JR		C, .err
	CALL	SHOW_DOC_STATUS
	RET
.err
	LD		HL, MSG_EXEC_FAIL
	JP		SET_STATUS

; HL -> the extension of DL_NAME (the part after the last '.'); empty if none.
DL_EXT_PTR
	LD		HL, DL_NAME
	LD		BC, 0					; BC = ext pointer (after the last '.'), 0 = none
.l
	LD		A, (HL)
	OR		A
	JR		Z, .done
	CP		'.'
	JR		NZ, .next
	LD		B, H					; ext candidate = char after this '.'
	LD		C, L
	INC		BC
.next
	INC		HL
	JR		.l
.done
	LD		A, B
	OR		C
	JR		Z, .noext
	LD		H, B
	LD		L, C
	RET
.noext
	LD		HL, EMPTYSTR
	RET

; Build FILE_ABS = <EXE dir> + DL_PATH (absolute path of the saved file). If the
; EXE dir is unknown (AppInfo failed) EXE_DIR is empty and the path stays relative.
BUILD_FILE_ABS
	LD		HL, EXE_DIR
	LD		DE, FILE_ABS
	CALL	COPYZ					; copy dir (no NUL); DE -> end
	LD		HL, DL_PATH
	CALL	COPYZ					; append "DOWNLOAD\<name>"
	XOR		A
	LD		(DE), A
	RET

; CF=1 if the "skip_ask_for_exec" setting is truthy (launch without asking),
; CF=0 if absent/false (ask the user first).
SKIP_ASK
	LD		HL, KEY_SKIPASK
	CALL	CFG.SETTING				; DE = value, CF=1 if not set
	JR		C, .ask					; not set -> ask
	LD		A, (DE)					; truthy if it starts 1/y/Y/t/T
	CP		'1'
	JR		Z, .skip
	CP		'y'
	JR		Z, .skip
	CP		'Y'
	JR		Z, .skip
	CP		't'
	JR		Z, .skip
	CP		'T'
	JR		Z, .skip
.ask
	OR		A						; CF=0 -> ask the user
	RET
.skip
	SCF								; CF=1 -> launch without asking
	RET

; Incomplete transfer: ask whether to delete + retry or keep the partial file.
; Out: CF=0 retry (R/r), CF=1 keep (any other key; the safe default - no
; surprise deletion on a stray keypress).
CONFIRM_KEEP
	LD		HL, MSG_ASK_KEEP
	CALL	SET_STATUS
	CALL	INP_WAIT_RELEASE
.poll
	CALL	CLOCK_TICK
	CALL	KBD.SCAN
	JR		Z, .poll
	CP		'R'
	JR		Z, .retry
	CP		'r'
	JR		Z, .retry
	CALL	INP_WAIT_RELEASE
	SCF								; keep
	RET
.retry
	CALL	INP_WAIT_RELEASE
	OR		A						; CF=0
	RET

; Incomplete page: ask whether to reload or show what loaded (replaces the old
; silent auto-retry). Out: CF=0 = reload (R/r), CF=1 = show what loaded (any other
; key - the safe default, so a stray keypress never re-hits the network).
CONFIRM_RELOAD
	LD		HL, MSG_ASK_RELOAD
	CALL	SET_STATUS
	CALL	INP_WAIT_RELEASE
.poll
	CALL	CLOCK_TICK
	CALL	KBD.SCAN
	JR		Z, .poll
	CP		'R'
	JR		Z, .reload
	CP		'r'
	JR		Z, .reload
	CALL	INP_WAIT_RELEASE
	SCF								; show what loaded
	RET
.reload
	CALL	INP_WAIT_RELEASE
	OR		A						; CF=0
	RET

; Ask whether to open the file. Out: CF=0 if yes (Y/y), CF=1 otherwise.
CONFIRM_OPEN
	LD		HL, MSG_OPEN_ASK
	CALL	SET_STATUS
	CALL	INP_WAIT_RELEASE
.poll
	CALL	CLOCK_TICK
	CALL	KBD.SCAN
	JR		Z, .poll
	CP		'Y'
	JR		Z, .yes
	CP		'y'
	JR		Z, .yes
	CALL	INP_WAIT_RELEASE
	SCF
	RET
.yes
	CALL	INP_WAIT_RELEASE
	OR		A						; CF=0
	RET

; Launch the program whose command line is in HL (program + args). Saves/restores
; SP (Dss.Exec changes it), re-maps our WIN2 scratch page and re-asserts 80x32 text
; on return (the child may have changed both). Out: A=exit code, CF=1 on launch
; error (FM idiom: HL=cmdline, BC=#0040).
; Clear the screen to the standard attributes (black paper, non-bright white
; ink = ATTR_NORM) and announce the program we are about to launch, so the
; external viewer starts from a clean, predictable console. In: HL = command
; line (lives in the WIN2 page, CFG_CMD_BUF). Preserves HL for the Dss.Exec that
; follows. (PChars prints fine straight from WIN2 - verified on target.)
SHOW_EXEC_BANNER
	PUSH	HL
	CALL	TERM.CLS				; black background, non-bright white text
	LD		DE, 0					; D=row 0, E=col 0
	CALL	TERM.LOCATE
	LD		HL, MSG_RUNEXT			; "Running external viewer:\r\n"
	CALL	TERM.PUTS
	POP		HL
	PUSH	HL						; HL = command line
	CALL	TERM.PUTS				; print the command line straight from WIN2
	LD		HL, MSG_CRLF			; end the line so the child starts on a fresh row
	CALL	TERM.PUTS
	POP		HL						; restore command pointer for Dss.Exec
	RET

EXEC_PROGRAM
	CALL	SHOW_EXEC_BANNER		; clean screen + "Running external viewer:" + cmd
	; Dss.Exec saves the parent's WIN1/WIN2/WIN3 page registers (and SP) and
	; restores them on return; our WIN2 page is a GetMem block tagged to our task,
	; so the child can't allocate over it. So the WIN2 runtime stack survives the
	; exec unchanged - no stack juggling needed. (exec_sp is just belt-and-braces;
	; it's a WIN1 var, valid to read whatever WIN2 holds.)
	LD		(exec_sp), SP
	LD		BC, 0x0040				; B=0 (short name), C=#40 (Dss.Exec)
	RST		DSS						; -> A=exit code, CF=1 error
	LD		SP, (exec_sp)
	PUSH	AF
	CALL	CHDIR_EXE				; cwd is process state (not a window) - restore it
	LD		A, (video_page)			; restore the text-screen page captured at startup
	LD		B, A
	LD		A, DSS_VMOD_T80			; re-assert 80x32 text mode
	LD		C, DSS_SETVMOD
	RST		DSS
	; The child ran with the ISA card in an unknown state (DSS/Exec and the viewer
	; may have touched the ISA window / UART), so drop our cached "card is up"
	; flag; the next fetch re-runs NET.INIT (re-init UART + AT, drain stale bytes,
	; no ISA_RESET so the NETUP Wi-Fi session is preserved). Without this the next
	; TCP.OPEN talks to a disturbed card -> "Connect failed".
	CALL	INVALIDATE_NET
	POP		AF
	RET

; Drop the cached "ESP is initialised" flag so the next fetch re-runs NET.INIT
; (full UART re-init + AT + drain, no ISA_RESET -> Wi-Fi session preserved). This
; is a RECOVERY: a cached net_inited=1 makes a fetch skip init and reuse the open
; UART/ESP session; if that session has gone stale the connect hangs on
; "Fetching..." and, because the flag stays 1, never recovers ("link dies"). A
; forced re-init clears any stale UART/ESP state.
;   NOTE: this is NOT because file I/O corrupts the card. The kit brackets every
;   UART access with ISA_OPEN/ISA_CLOSE (which save/restore WIN3), so a file
;   read/write between network ops is safe - that's exactly why a chunked download
;   (interleaved RECV + FILE.WRITE on one open socket) works. EXEC_PROGRAM re-inits
;   for a different reason (a child program can reprogram the ISA card directly).
; Trashes A.
INVALIDATE_NET
	XOR		A
	LD		(net_inited), A
	RET

; Derive an 8.3 filename from the selector DL_SEL into DL_NAME, then build
; DL_PATH = "DOWNLOAD\<name>". Non-FAT chars are sanitised; an empty basename
; falls back to a default. Trashes A/BC/DE/HL.
MAKE_DLNAME
	; 1) find the basename start (after the last '/' or '\') in DE.
	LD		HL, DL_SEL
	LD		D, H
	LD		E, L
.fb
	LD		A, (HL)
	OR		A
	JR		Z, .fbdone
	CP		'/'
	JR		Z, .sep
	CP		'\'
	JR		Z, .sep
	INC		HL
	JR		.fb
.sep
	INC		HL
	LD		D, H
	LD		E, L					; basename start = char after the separator
	JR		.fb
.fbdone
	LD		A, (DE)
	OR		A
	JP		Z, .default				; empty basename
	; 2) copy up to 8 name chars (until '.' or end) into DL_NAME.
	EX		DE, HL					; HL = src basename
	LD		DE, DL_NAME				; dst
	LD		B, 8
.nloop
	LD		A, (HL)
	OR		A
	JR		Z, .namedone			; end, no extension
	CP		'.'
	JR		Z, .extstart
	CALL	SANITIZE_CH
	LD		(DE), A
	INC		DE
	INC		HL
	DJNZ	.nloop
	; name longer than 8: skip the rest until '.' or end
.skipname
	LD		A, (HL)
	OR		A
	JR		Z, .namedone
	CP		'.'
	JR		Z, .extstart
	INC		HL
	JR		.skipname
.extstart
	INC		HL						; skip the '.'
	LD		A, (HL)
	OR		A
	JR		Z, .namedone			; trailing dot, no extension
	LD		A, '.'
	LD		(DE), A
	INC		DE
	LD		B, 3
.eloop
	LD		A, (HL)
	OR		A
	JR		Z, .namedone
	CALL	SANITIZE_CH
	LD		(DE), A
	INC		DE
	INC		HL
	DJNZ	.eloop
.namedone
	XOR		A
	LD		(DE), A					; NUL-terminate DL_NAME
	JR		.buildpath
.default
	LD		HL, DEF_DLNAME
	LD		DE, DL_NAME
	LD		B, 16
	CALL	STRCPYN
.buildpath
	LD		HL, DL_DIRPFX			; "DOWNLOAD\"
	LD		DE, DL_PATH
	CALL	COPYZ					; DE -> end (no NUL)
	LD		HL, DL_NAME
	CALL	COPYZ
	XOR		A
	LD		(DE), A					; NUL-terminate DL_PATH
	RET

; Map A to a FAT-safe uppercase filename char (A-Z 0-9 - _ kept, else '_').
SANITIZE_CH
	CP		'a'
	JR		C, .chk
	CP		'z' + 1
	JR		NC, .chk
	SUB		0x20					; to uppercase
.chk
	CP		'0'
	JR		C, .bad
	CP		'9' + 1
	JR		C, .good
	CP		'A'
	JR		C, .bad
	CP		'Z' + 1
	JR		C, .good
	CP		'-'
	RET		Z
	CP		'_'
	RET		Z
.bad
	LD		A, '_'
.good
	RET

; ------------------------------------------------------
; Parse LINE_BUF into row_type + p_disp/p_sel/p_host/p_port (pointers into
; LINE_BUF). TABs are replaced with NUL; absent fields point at an empty string.
; ------------------------------------------------------
PARSE_ROW
	LD		DE, EMPTYSTR
	LD		(p_sel), DE
	LD		(p_host), DE
	LD		(p_port), DE
	LD		A, (DOC_TYPE_CUR)
	CP		'1'
	JR		Z, .menu
	; text document (type 0): the line is raw text, NO type byte and no TAB
	; fields - the whole line is the display.
	LD		HL, LINE_BUF
	LD		(p_disp), HL
	XOR		A
	LD		(row_type), A			; 0 = plain text line (non-selectable)
	RET
.menu
	LD		HL, LINE_BUF
	LD		A, (HL)
	LD		(row_type), A
	INC		HL
	LD		(p_disp), HL
	CALL	SCAN_FIELD				; end of display
	RET		NC						; info line: no further fields
	LD		(p_sel), HL
	CALL	SCAN_FIELD
	RET		NC
	LD		(p_host), HL
	CALL	SCAN_FIELD
	RET		NC
	LD		(p_port), HL
	; gopher+ servers (e.g. nihirash.net) append a 5th "\t+" field after the port
	; to flag gopher+ support. Terminate the port field too so it does not inherit
	; that "\t+" tail - otherwise PORT_CUR becomes "70\t+", the AT+CIPSTART port is
	; malformed, and every link on such a menu fails to connect. (For a normal menu
	; the port is the last field: SCAN_FIELD hits the line NUL and changes nothing.)
	CALL	SCAN_FIELD
	RET

; Advance HL to the next field. If a TAB is found it is replaced with NUL and
; HL points past it (CF=1); at NUL, CF=0 (no more fields).
SCAN_FIELD
.l
	LD		A, (HL)
	OR		A
	JR		Z, .end
	CP		9
	JR		Z, .tab
	INC		HL
	JR		.l
.tab
	LD		(HL), 0
	INC		HL
	SCF
	RET
.end
	OR		A
	RET

; ------------------------------------------------------
; History: push current nav before following a link; pop to go back.
; ------------------------------------------------------
PUSH_HIST
	LD		A, (hist_sp)
	CP		HIST_MAX
	JR		C, .haveroom
	; full: free the oldest level's cached pages, then shift the array down
	LD		HL, HIST_DATA + HR_DOCSTATE
	CALL	DOC.FREE_STATE
	LD		HL, HIST_DATA + HR_SIZE
	LD		DE, HIST_DATA
	LD		BC, (HIST_MAX - 1) * HR_SIZE
	LDIR
	LD		A, HIST_MAX - 1
	LD		(hist_sp), A
.haveroom
	LD		A, (hist_sp)
	CALL	HREC_ADDR				; HL = record address
	PUSH	HL
	LD		A, (cur_kind)
	LD		(HL), A					; +HR_KIND
	POP		HL
	PUSH	HL
	LD		DE, HR_HOST
	ADD		HL, DE
	EX		DE, HL
	LD		HL, HOST_CUR
	LD		B, 64
	CALL	STRCPYN
	POP		HL
	PUSH	HL
	LD		DE, HR_PORT
	ADD		HL, DE
	EX		DE, HL
	LD		HL, PORT_CUR
	LD		B, 8
	CALL	STRCPYN
	POP		HL
	PUSH	HL
	LD		DE, HR_SEL
	ADD		HL, DE
	EX		DE, HL
	LD		HL, SEL_CUR
	LD		B, 200
	CALL	STRCPYN
	POP		HL
	PUSH	HL
	LD		DE, HR_TYPE
	ADD		HL, DE
	LD		A, (DOC_TYPE_CUR)
	LD		(HL), A
	POP		HL
	PUSH	HL
	LD		DE, HR_SELIDX
	ADD		HL, DE
	EX		DE, HL
	LD		HL, (sel_index)
	CALL	STORE_HL_DE
	POP		HL
	LD		DE, HR_TOPIDX
	ADD		HL, DE
	EX		DE, HL
	LD		HL, (top_index)
	CALL	STORE_HL_DE
	; title
	LD		A, (hist_sp)
	CALL	HREC_ADDR
	LD		DE, HR_TITLE
	ADD		HL, DE
	EX		DE, HL
	LD		HL, DOC_TITLE
	LD		B, TITLE_MAX
	CALL	STRCPYN
	; cache the document itself (page chain) so Back needs no re-fetch
	LD		A, (hist_sp)
	CALL	HREC_ADDR
	LD		DE, HR_DOCSTATE
	ADD		HL, DE
	CALL	DOC.SAVE_STATE
	LD		A, (hist_sp)
	INC		A
	LD		(hist_sp), A
	RET

POP_HIST
	LD		A, (hist_sp)
	OR		A
	RET		Z
	DEC		A
	LD		(hist_sp), A
	CALL	HREC_ADDR				; HL = record
	PUSH	HL
	LD		A, (HL)
	LD		(cur_kind), A
	POP		HL
	PUSH	HL
	LD		DE, HR_HOST
	ADD		HL, DE
	LD		DE, HOST_CUR
	LD		B, 64
	CALL	STRCPYN
	POP		HL
	PUSH	HL
	LD		DE, HR_PORT
	ADD		HL, DE
	LD		DE, PORT_CUR
	LD		B, 8
	CALL	STRCPYN
	POP		HL
	PUSH	HL
	LD		DE, HR_SEL
	ADD		HL, DE
	LD		DE, SEL_CUR
	LD		B, 200
	CALL	STRCPYN
	POP		HL
	PUSH	HL
	LD		DE, HR_TYPE
	ADD		HL, DE
	LD		A, (HL)
	LD		(DOC_TYPE_CUR), A
	POP		HL
	PUSH	HL
	LD		DE, HR_SELIDX
	ADD		HL, DE
	CALL	LOAD_HL_FROM_HL
	LD		(sel_index), HL
	POP		HL
	PUSH	HL
	LD		DE, HR_TOPIDX
	ADD		HL, DE
	CALL	LOAD_HL_FROM_HL
	LD		(top_index), HL
	POP		HL
	LD		DE, HR_TITLE
	ADD		HL, DE
	LD		DE, DOC_TITLE
	LD		B, TITLE_MAX
	CALL	STRCPYN
	; restore the cached document (page chain) - no re-fetch
	LD		A, (hist_sp)
	CALL	HREC_ADDR
	LD		DE, HR_DOCSTATE
	ADD		HL, DE
	CALL	DOC.LOAD_STATE
	RET

; HL = HIST_DATA + A*HR_SIZE
HREC_ADDR
	LD		HL, HIST_DATA
	OR		A
	RET		Z
	LD		B, A
	LD		DE, HR_SIZE
.l
	ADD		HL, DE
	DJNZ	.l
	RET

; Store HL (16-bit little-endian) at (DE).
STORE_HL_DE
	LD		A, L
	LD		(DE), A
	INC		DE
	LD		A, H
	LD		(DE), A
	RET

; Load 16-bit little-endian from (HL) into HL.
LOAD_HL_FROM_HL
	LD		A, (HL)
	INC		HL
	LD		H, (HL)
	LD		L, A
	RET

; ------------------------------------------------------
; String helpers.
; ------------------------------------------------------
; Copy ASCIIZ HL->DE including the NUL, up to B-1 chars then forced NUL.
STRCPYN
.l
	LD		A, B
	CP		1
	JR		Z, .term
	LD		A, (HL)
	LD		(DE), A
	OR		A
	RET		Z
	INC		HL
	INC		DE
	DEC		B
	JR		.l
.term
	XOR		A
	LD		(DE), A
	RET

; Copy ASCIIZ HL->DE without the NUL. Out: DE past last char, HL past NUL.
COPYZ
.l
	LD		A, (HL)
	INC		HL
	OR		A
	RET		Z
	LD		(DE), A
	INC		DE
	JR		.l

